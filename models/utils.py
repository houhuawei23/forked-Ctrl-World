"""
机器人控制与运动学辅助函数（Franka Panda FK、键盘交互 rollout）。

**核心内容**
    - ``get_fk_solution``：基于固定 DH 参数表的正运动学，返回 4×4 齐次变换矩阵。
    - ``key_board_control``：将单字符键盘指令映射为 5 步笛卡尔轨迹（xyz + rpy + gripper），含任务
      ``1799`` 下的硬编码关键位姿与工作空间 clip。

**依赖**
    ``numpy``；无 PyTorch 依赖。

**复杂度**
    ``get_fk_solution``：对 7+ 关节链做固定次数矩阵乘，时间 O(1)，空间 O(1)。
    ``key_board_control``：时间 O(1)，输出形状固定 ``(5, 7)``。
"""

from __future__ import annotations

import numpy as np

################# Franka Panda Forward Kinematics ##############################


def get_fk_solution(joint_angles: np.ndarray) -> np.ndarray:
    """
    计算 Franka Panda 末端（含固定手指链）相对基座的 4×4 齐次变换矩阵。

    **Args**
        joint_angles: 长度至少为 7 的关节角向量（弧度），对应 DH 表中各关节 ``theta``。

    **Returns**
        ``np.ndarray``，形状 ``(4, 4)``，最后一列为位置（米级，与 DH 参数一致）。

    **说明**
        使用仓库内嵌的 DH 表（含腕部与夹爪固定段）；非完整逆运动学，仅用于调试或可视化。
    """

    def get_tf_mat(i: int, dh: list) -> np.ndarray:
        a = dh[i][0]
        d = dh[i][1]
        alpha = dh[i][2]
        theta = dh[i][3]
        q = theta

        return np.array(
            [
                [np.cos(q), -np.sin(q), 0, a],
                [np.sin(q) * np.cos(alpha), np.cos(q) * np.cos(alpha), -np.sin(alpha), -np.sin(alpha) * d],
                [np.sin(q) * np.sin(alpha), np.cos(q) * np.sin(alpha), np.cos(alpha), np.cos(alpha) * d],
                [0, 0, 0, 1],
            ]
        )

    dh_params = [
        [0, 0.333, 0, joint_angles[0]],
        [0, 0, -np.pi / 2, joint_angles[1]],
        [0, 0.316, np.pi / 2, joint_angles[2]],
        [0.0825, 0, np.pi / 2, joint_angles[3]],
        [-0.0825, 0.384, -np.pi / 2, joint_angles[4]],
        [0, 0, np.pi / 2, joint_angles[5]],
        [0.088, 0, np.pi / 2, joint_angles[6]],
        [0, 0.107, 0, 0],
        [0, 0, 0, -np.pi / 4],
        [0.0, 0.1034, 0, 0],
    ]

    T = np.eye(4)
    for i in range(7 + 1):
        T = T @ get_tf_mat(i, dh_params)
    return T


